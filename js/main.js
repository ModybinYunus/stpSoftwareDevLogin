var x = document.getElementById("signUp");
var y = document.getElementById("signIn");
var z = document.getElementById("mybtn");

// Toggle Function 

function signIn(){
    x.style.left ="-600px";
    y.style.left ="120px";
    z.style.left ="0px";
}
function signUp(){
    y.style.left ="-600px";
    x.style.left ="120px";
    z.style.left ="130px";
}
